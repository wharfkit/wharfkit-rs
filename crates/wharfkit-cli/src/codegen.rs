#[cfg(test)]
mod emit_header_tests {
    use super::*;

    fn fixture_ctx<'a>(abi: &'a ABI, chain: &'a ResolvedChain) -> EmitContext<'a> {
        EmitContext {
            abi,
            chain,
            account: "eosio.token",
            abi_hash: "deadbeef",
            tool_version: "0.1.0",
        }
    }

    #[test]
    fn header_includes_required_fields() {
        let abi: ABI = serde_json::from_str(r#"{"version":"eosio::abi/1.2","types":[],"structs":[],"actions":[],"tables":[],"variants":[]}"#).unwrap();
        let chain = resolve_chain("jungle4");
        let ctx = fixture_ctx(&abi, &chain);
        let out = emit_header(&ctx);
        assert!(out.contains("@generated by wharfkit-cli v0.1.0"));
        assert!(out.contains("Contract: eosio.token"));
        assert!(out.contains("Chain:    Jungle 4 Test Network"));
        assert!(out.contains("ABI hash: deadbeef"));
        assert!(out.contains("wharfkit-cli codegen --chain jungle4 --account eosio.token"));
        assert!(out.contains("pub const ACCOUNT"));
        assert!(out.contains("pub const ABI_HASH: &str = \"deadbeef\";"));
    }

    #[test]
    fn header_does_not_include_timestamp() {
        let abi: ABI = serde_json::from_str(r#"{"version":"eosio::abi/1.2","types":[],"structs":[],"actions":[],"tables":[],"variants":[]}"#).unwrap();
        let chain = resolve_chain("jungle4");
        let ctx = fixture_ctx(&abi, &chain);
        let out1 = emit_header(&ctx);
        let out2 = emit_header(&ctx);
        assert_eq!(out1, out2);
        assert!(!out1.contains("2026"));
    }

    #[test]
    fn header_imports_serializer_encoder_decoder() {
        let abi: ABI = serde_json::from_str(r#"{"version":"eosio::abi/1.2","types":[],"structs":[],"actions":[],"tables":[],"variants":[]}"#).unwrap();
        let chain = resolve_chain("jungle4");
        let ctx = fixture_ctx(&abi, &chain);
        let out = emit_header(&ctx);
        assert!(
            out.contains("Encoder") && out.contains("Decoder"),
            "header must import Encoder and Decoder for StructPacker / EnumPacker; got:\n{out}"
        );
    }

    #[test]
    fn header_does_not_import_unused_enum_packer_when_no_variants() {
        let abi: ABI = serde_json::from_str(r#"{"version":"eosio::abi/1.2","types":[],"structs":[{"name":"x","base":"","fields":[]}],"actions":[],"tables":[],"variants":[]}"#).unwrap();
        let chain = resolve_chain("jungle4");
        let ctx = fixture_ctx(&abi, &chain);
        let out = emit_header(&ctx);
        assert!(
            !out.contains("EnumPacker"),
            "header must not import EnumPacker when the ABI has no variants; got:\n{out}"
        );
        assert!(
            out.contains("StructPacker"),
            "header still needs StructPacker for the struct in this fixture; got:\n{out}"
        );
    }

    #[test]
    fn header_imports_enum_packer_when_variants_present() {
        let abi: ABI = serde_json::from_str(r#"{"version":"eosio::abi/1.2","types":[],"structs":[],"actions":[],"tables":[],"variants":[{"name":"v","types":["int32"]}]}"#).unwrap();
        let chain = resolve_chain("jungle4");
        let ctx = fixture_ctx(&abi, &chain);
        let out = emit_header(&ctx);
        assert!(
            out.contains("EnumPacker"),
            "header must import EnumPacker when the ABI has variants; got:\n{out}"
        );
    }
}

